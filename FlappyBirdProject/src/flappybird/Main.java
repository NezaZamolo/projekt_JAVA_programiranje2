package flappybird;

/**
 * The entry point for the Flappy Bird game application.
 * This class contains the `main` method to start the game.
 */
public class Main {

    /**
     * The main method to launch the Flappy Bird game.
     * It creates an instance of `GameFrame` to initialize the game window.
     *
     * @param args Command-line arguments (not used).
     */
    public static void main(String[] args) {
        new GameFrame(); // Initialize and display the game frame
    }
}